char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char *dst;

	dst = dest;
	while (n >= 1)
	{
		if (*src == '\0')
			*dest++ = '\0';
		*dest++ = *src++;
		n--;
	}
	return (dst);
}
